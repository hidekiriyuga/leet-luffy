class Solution {
  public:
    int longestOnes(int n, vector<int>& nums, int k) {
        // Code here
        int l=0,r=0,z=0,ans=0;
        while(r<nums.size()){
            if(nums[r]==0)z++;
            if(z>k){
                if(nums[l]==0)z--;
                l++;
            }
            else{
                ans=max(ans,r-l+1);
            }
            r++;
        }
        return ans;
    }
};

